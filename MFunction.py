class mfunction():

    def plus(a,b):
        return a + b
    
    def minus(a,b):
        return a-b
    
    def multiply(a,b):
        return a*b
    
    def divide(a,b):
        return a/b
    
    def x10(a,b):
        return a*10**b
    
    def square(a,b):
        return a**b






